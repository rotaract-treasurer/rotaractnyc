// Helper to create dates relative to now
const daysFromNow = (days: number) => {
  const d = new Date()
  d.setDate(d.getDate() + days)
  return d
}

const hoursFromNow = (hours: number) => {
  const d = new Date()
  d.setHours(d.getHours() + hours)
  return d
}

